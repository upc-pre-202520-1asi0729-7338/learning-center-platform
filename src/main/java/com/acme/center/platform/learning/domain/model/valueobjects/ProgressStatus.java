package com.acme.center.platform.learning.domain.model.valueobjects;

/**
 * Progress status enumeration.
 * <p>
 * This enum represents the possible statuses of progress in a learning path or enrollment.
 * </p>
 */
public enum ProgressStatus {
    /** The progress has not started. */
    NOT_STARTED,
    /** The progress has started. */
    STARTED,
    /** The progress is completed. */
    COMPLETED
}
